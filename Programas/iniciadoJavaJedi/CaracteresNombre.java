package Programas.iniciadoJavaJedi;
/*
 * En Java, las comillas dobles ("") se utilizan para delimitar cadenas de caracteres (tipo String), 
 * mientras que las comillas simples ('') se utilizan para delimitar caracteres (tipo char).
 */
public class CaracteresNombre {
    public static void main(String[] args) {
        // Declarar variables char para cada letra del nombre
        char letra1 = 'J';
        char letra2 = 'o';
        char letra3 = 'h';
        char letra4 = 'n';

        // Mostrar las letras por pantalla
        System.out.println(letra1);
        System.out.println(letra2);
        System.out.println(letra3);
        System.out.println(letra4);
    }
}
